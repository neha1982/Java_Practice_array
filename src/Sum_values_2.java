
//Java program to sum values of an array.
public class Sum_values_2 {
    public static void main(String[] args) {
        int n1[] ={20, 40, 50, 60, 70};
        int sum = 0;
        int i;
        for (i=0; i<n1.length; i++);

            sum += i;

        System.out.println("Sum is: " + sum);
    }


}
